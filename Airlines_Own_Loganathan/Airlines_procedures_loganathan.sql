CREATE OR REPLACE PROCEDURE AIRLINE
IS
TYPE VARCHAR_TYPE IS TABLE OF VARCHAR(100);
A_AIRLINENAME VARCHAR_TYPE;
HEAD_ADD VARCHAR_TYPE;
BEGIN
  SELECT
    AIRLINE_NAME,HEAD_ADDRESS
  BULK COLLECT   
  INTO
    A_AIRLINENAME,HEAD_ADD  
  FROM
    AIRLINES;
  FOR I IN 1..A_AIRLINENAME.COUNT
  LOOP 
    DBMS_OUTPUT.PUT_LINE(A_AIRLINENAME(I)||' '||HEAD_ADD(I)); 
  END LOOP;  
END;


BEGIN
    AIRLINE();
END;


CREATE OR REPLACE PROCEDURE DOMSTIC_FLIGINTS
IS
  CURSOR cur
  IS
    SELECT
      *
    FROM
      FLIGHTS
    WHERE
      FLIGHT_TYPE ='Domestic';
BEGIN
  FOR I IN cur LOOP
    DBMS_OUTPUT.PUT_LINE(I.FLIGHT_NUMBER||'  '||I.DESTINATION||'   '||I.SEAT_CAPACITY);
  END LOOP;
END;
/

EXEC DOMSTIC_FLIGINTS();

