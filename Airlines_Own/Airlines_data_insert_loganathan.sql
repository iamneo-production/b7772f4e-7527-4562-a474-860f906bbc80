CREATE OR REPLACE PROCEDURE ADD_PASSENGER(
    F_NAME VARCHAR,M_NAME VARCHAR,L_NAME VARCHAR,DOB DATE,GEN CHAR
)
IS
AGE NUMBER;
PASS_TYPE VARCHAR(32);
BEGIN
AGE := ROUND((SYSDATE - DOB)/365.25, 0);
IF AGE >= 60 THEN
    PASS_TYPE := 'CITIZEN';
ELSIF AGE < 60 AND AGE >= 18 THEN
    PASS_TYPE := 'ADULT';
ELSE
    PASS_TYPE := 'CHILDREN';
END IF;
INSERT INTO PASSENGER VALUES(PASS_SEQ.NEXTVAL,F_NAME,M_NAME,L_NAME,DOB,AGE,GEN,PASS_TYPE);
END;
/


